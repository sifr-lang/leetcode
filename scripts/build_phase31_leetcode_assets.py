DEMO_CASE_IDS = ["0001", "0009", "0069", "0070", "0100"]


def main() -> None:
    verification_dir = REPO_ROOT / "verification" / "leetcode"
    seed_manifest_path = verification_dir / "phase31_seed_corpus.json"
    inventory_path = verification_dir / "phase31_corpus_inventory.json"
    summary_path = verification_dir / "phase31_seed_summary.json"
    demo_manifest_path = verification_dir / "phase31_demo_corpus.json"

    seed_summary = validate_seed_manifest(SEED_CASES)
    seed_case_ids = {entry["id"] for entry in SEED_CASES}
    inventory = build_inventory(seed_case_ids)
    write_json(
        seed_manifest_path,
        {
            "phase": 31,
            "name": "phase31_seed_corpus",
            "case_count": len(SEED_CASES),
            "cases": SEED_CASES,
        },
    )
    write_json(
        inventory_path,
        {
            "phase": 31,
            "name": "phase31_corpus_inventory",
            "summary": inventory_summary(inventory),
            "cases": inventory,
        },
    )
    write_json(summary_path, seed_summary)
    write_json(
        demo_manifest_path,
        {
            "phase": 31,
            "name": "phase31_demo_corpus",
            "case_count": len(DEMO_CASE_IDS),
            "cases": [case for case in SEED_CASES if case["id"] in DEMO_CASE_IDS],
        },
    )

    print(f"wrote {seed_manifest_path.relative_to(REPO_ROOT)}")
    print(f"wrote {inventory_path.relative_to(REPO_ROOT)}")
    print(f"wrote {summary_path.relative_to(REPO_ROOT)}")
    print(f"wrote {demo_manifest_path.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
